# Typecasting = the process of converting a variable from
#               one data type to another

name = "Kyle Goodman"
age = 20
gpa = 3.92
is_student = True

# Convert GPA to an integer
intgpa = int(gpa)
print(intgpa) # print 3

# Convert age to a float
floatage = float(age)
print(floatage) # print 20.0

# Convert age to string
strage = str(age)
print(strage) # print 20

# Convert name to Boolean
boolname = bool(name)
print(boolname) # print True, if it were empty, False

